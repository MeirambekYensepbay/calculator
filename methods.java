package calculator;

public class methods {
	
	double plus(double a, double b){
		a =  a + b;
		return a;
	}
	double minus(double a, double b){
		return a-b;
	}
	double divide(double a, double b){
		return a/b;
	}
	double multiply(double a, double b){
		return a*b;
	}
	double percent(double a, double b){
		return a*b/100;
	}
}
